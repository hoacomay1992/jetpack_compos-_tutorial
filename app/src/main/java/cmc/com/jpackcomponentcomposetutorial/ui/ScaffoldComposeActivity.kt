 * Trong Material Design, Scaffold là cấu trúc cơ bản cung cấp nền tảng
 * được tiêu chuẩn hóa cho các giao diện người dùng phức tạp.
 * Nó kết hợp các phần khác nhau của giao diện người dùng,
 * chẳng hạn như app bar và FloatActionButton,
 * mang lại cho ứng dụng một giao diện mạch lạc.
 */
class ScaffoldComposeActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent {
            JpackComponentComposeTutorialTheme {
                // A surface container using the 'background' color from the theme
                Surface(modifier = Modifier.fillMaxSize(), color = MaterialTheme.colorScheme.background) {
                    ScaffoldHomeCompose()
                }
            }
        }
    }
}

